package draw;

/** A numeric value in the interpreter.
 * @author Ken Szubzda cs61b-ek
 */
class Number extends Value {

    /** A Number representing VALUE. */
    Number(double value) {
        _value = value;
    }

    @Override
    double doubleValue() {
        return _value;
    }

    /** The value I represent. */
    private double _value;
}
